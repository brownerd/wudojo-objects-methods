// String.prototype.localeCompare()
// https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Global_Objects/String/localeCompare
//

// This is great because you can use it to test if alphabetic order of words and letters in different langauges

export const localeCompareFN = (referenceStr, compareStr) => referenceStr.localeCompare(compareStr)
